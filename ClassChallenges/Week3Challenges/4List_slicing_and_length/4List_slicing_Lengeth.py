colors = ['red', 'blue', 'green', 'yellow', 'purple'] #create a variable for a list of colours
slice_colors = colors[1:3] #slice the list to get the 2nd and 3rd colours
print(slice_colors) #print the edited list
print(len(slice_colors)) #print the length of the new list