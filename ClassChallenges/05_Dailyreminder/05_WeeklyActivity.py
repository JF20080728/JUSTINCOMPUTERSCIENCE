DAY = input("what is the current day of the week ")
if DAY == "Monday":
    print("Start your week with a workout!")
elif DAY == "Tuesday":
    print("It's a great day to read a book!")
elif DAY == "Wednesday":
    print("Mid-week movie night!")
elif DAY == "Thursday":
    print("Try a new recipe!")
elif DAY == "Friday":
    print("Relax and enjoy the weekend!")
elif DAY == "Saturday":
    print("Go for a hike!")
elif DAY == "Sunday":
    print("Prepare for the week ahead with some self-care.")
else :
    print("unable to identify the day of the week, please try again")